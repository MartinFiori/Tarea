// ========== counter1
function contador1() {
    let counter1 = document.querySelector("#counter1");
    counter1.innerHTML = largo1.length;


    let plus1 = document.querySelector(".plus1")
    plus1.addEventListener('click', () => {
        largo1.push(1)
        counter1.innerHTML = ""
        counter1.innerHTML += largo1.length
    })

    counter1.innerHTML = largo1.length;

    let minus1 = document.querySelector(".minus1")
    minus1.addEventListener('click', () => {
        counter1.innerHTML = "";
        largo1.pop();
        counter1.innerHTML = largo1.length;
    })


    if (largo1.length < 0) {
        largo1.length = 0
    }
}


